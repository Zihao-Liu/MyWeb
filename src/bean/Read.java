package bean;

public class Read {
	private int readID;
	private int bookID;
	private int userID;
	private float score;
	public int getReadID() {
		return readID;
	}
	public void setReadID(int readID) {
		this.readID = readID;
	}
	public int getBookID() {
		return bookID;
	}
	public void setBookID(int bookID) {
		this.bookID = bookID;
	}
	public int getUserID() {
		return userID;
	}
	public void setUserID(int userID) {
		this.userID = userID;
	}
	public float getScore() {
		return score;
	}
	public void setScore(float score) {
		this.score = score;
	}
	
}
